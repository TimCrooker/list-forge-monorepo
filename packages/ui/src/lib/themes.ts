export type Theme = keyof typeof themes
export type ThemeConfig = (typeof themes)[Theme]

export function applyTheme(theme: Theme, mode: 'light' | 'dark') {
  const root = document.documentElement
  const colors = themes[theme].cssVars[mode]

  Object.entries(colors).forEach(([key, value]) => {
    root.style.setProperty(`--${key}`, value)
  })

  // Store theme in localStorage
  localStorage.setItem('theme', theme)
  localStorage.setItem('theme-mode', mode)
}

export function getStoredTheme(): { theme: Theme; mode: 'light' | 'dark' } {
  const theme = (localStorage.getItem('theme') as Theme) || 'default'
  const mode = (localStorage.getItem('theme-mode') as 'light' | 'dark') || 'light'
  return { theme, mode }
}
